public class Nums_150_300 { //Faça um algoritmo em Java que imprima todos os números de 150 a 300.

	public static void main(String[] args) {

		System.out.println("Programa que mostra todos os números entre 150 e 300\n");
		
		for(int i = 150; i <= 300; i++) {
			System.out.println(i);
		}
			
	}
}
